class calculator {

    constructor(num1Value, num2Value) {
        this.num1Value = num1Value;
        this.num2Value = num2Value;
    }

    add(num1Value, num2Value) {
        return num1Value + num2Value;
    };

    subtract(num1Value, num2Value) {
        return num1Value - num2Value;
    };

    multiply(num1Value, num2Value) {
        return num1Value * num2Value;
    };

    divide(num1Value, num2Value) {
        return num1Value / num2Value;
    };


}